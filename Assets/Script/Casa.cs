using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class Casa{
    public int val;
    public int x;
    public int y;

    public Casa(int v)
    {
        val = v;
        x = 0;
        y = 0;
    }

    public Casa()
    {
        val = 0;
        x = 0;
        y = 0;
    }

}
